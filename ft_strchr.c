char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if (s[i] == (char)c)
			return ((char *)&s[i]);
		if ((c < 0 || c > 127) || (s[i] < 0 || s[i] > 127))
			return (0);
		i++;
	}
	if (s[i] == 0)
		return ((char *)&s[i]);
	if (s[i] == c)
	{
		return ((char *)&s[i]);
	}
	return ("\0");
}
