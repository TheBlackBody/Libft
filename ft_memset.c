#include <string.h>

void	*ft_memset(void *p, int v, size_t len)
{
	size_t	i;
	char	*a;

	i = 0;
	a = (char *)p;
	while (i < len)
	{
		a[i] = v;
		i++;
	}
	return (p);
}
